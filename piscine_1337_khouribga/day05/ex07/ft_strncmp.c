int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int res;

	res = 0;
	while (*s1 || *s2)
	{
		while (n != 0)
		{
			if (*s1 == *s2)
			{
				s1++;
				s2++;
			}
			else
			{
				res = *s1 - *s2;
				return (res);
			}
			n--;
		}
	}
	return (res);
}
