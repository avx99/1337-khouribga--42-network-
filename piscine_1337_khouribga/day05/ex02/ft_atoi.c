int		ft_atoi(char *str)
{
	int n;
	int indice;

	n = 0;
	while (*str >= 0 && *str <= 32)
		str++;
	if (*str == '-')
	{
		indice = -1;
		str++;
	}
	else if (*str == '+')
	{
		indice = 1;
		str++;
	}
	while (*str >= '0' && *str <= '9' && *str)
	{
		n = n * 10 + *str - '0';
		str++;
	}
	if (indice == -1)
		n = n * (-1);
	return (n);
}
