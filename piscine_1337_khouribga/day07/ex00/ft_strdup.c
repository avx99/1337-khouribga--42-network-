#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*c;
	int		i;

	i = 0;
	while (src[i])
		i++;
	c = malloc(sizeof(char) * (i + 1));
	if (!c)
		return (NULL);
	i = 0;
	while (src[i])
	{
		c[i] = src[i];
		i++;
	}
	c[i] = '\0';
	return (c);
}
